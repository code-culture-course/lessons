"""
Пример 6: Добавление в файл

Демонстрирует:
- Открытие файла в режиме добавления
- Добавление нового содержимого без стирания старого
- Разница между режимами "w" и "a"
"""

print("Добавление новых записей в лог игры...")

# Режим "a" открывает для добавления
# Если файл не существует, он создаётся
# Если файл существует, новое содержимое добавляется в конец
file = open("game_log.txt", "a")

# Записать новые записи
file.write("\n--- Новая сессия ---\n")
file.write("Игрок вернулся в игру\n")
file.write("Игрок вошёл в подземелье\n")
file.write("Игрок победил дракона\n")
file.write("Игрок достиг 5 уровня\n")

file.close()

print("Новые записи добавлены в game_log.txt")
print()
print("Старое содержимое всё ещё там!")
print("Прочитайте файл с помощью 05_read_file.py для проверки.")
