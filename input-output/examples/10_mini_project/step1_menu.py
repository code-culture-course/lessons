def display_menu():
    """Показать опции меню."""
    print()
    print("=" * 40)
    print("МЕНЕДЖЕР СПИСКА ЗАДАЧ")
    print("=" * 40)
    print("1. Просмотреть задачи")
    print("2. Добавить задачу")
    print("3. Выход")
    print("=" * 40)


def view_tasks():
    """Отобразить все задачи (заглушка пока)."""
    print("\nВаши задачи:")
    print("(Задач пока нет - мы реализуем это далее)")


def add_task():
    """Добавить новую задачу (заглушка пока)."""
    task = input("\nВведите описание задачи: ")
    print(f"Задача '{task}' добавлена!")
    print("(Ещё не сохранена - мы реализуем это позже)")


# Главный цикл программы
print("Добро пожаловать в Менеджер списка задач!")

running = True
while running:
    display_menu()
    choice = input("\nВыберите опцию (1-3): ")
    
    if choice == "1":
        view_tasks()
    elif choice == "2":
        add_task()
    elif choice == "3":
        print("\nДо свидания!")
        running = False
    else:
        print("\nНеверный выбор. Пожалуйста, выберите 1-3.")

print("Спасибо за использование Менеджера списка задач!")

