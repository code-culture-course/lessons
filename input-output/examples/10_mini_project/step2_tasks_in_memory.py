"""
Мини-проект Шаг 2: Задачи в памяти

Что мы изучаем:
- Хранение данных в списке
- Добавление элементов в список
- Отображение элементов из списка

Задачи работают пока программа запущена, но исчезают когда она останавливается.
"""

# Глобальный список для хранения задач
tasks = []


def display_menu():
    """Показать опции меню."""
    print()
    print("=" * 40)
    print("МЕНЕДЖЕР СПИСКА ЗАДАЧ")
    print("=" * 40)
    print("1. Просмотреть задачи")
    print("2. Добавить задачу")
    print("3. Выход")
    print("=" * 40)


def view_tasks():
    """Отобразить все задачи."""
    print()
    if len(tasks) == 0:
        print("Задач пока нет. Добавьте несколько!")
    else:
        print("Ваши задачи:")
        for i, task in enumerate(tasks, 1):
            print(f"  {i}. {task}")


def add_task():
    """Добавить новую задачу в список."""
    task = input("\nВведите описание задачи: ")
    if task.strip():  # Добавить только если не пустая
        tasks.append(task)
        print(f"✓ Задача добавлена!")
    else:
        print("Задача не может быть пустой.")


# Главный цикл программы
print("Добро пожаловать в Менеджер списка задач!")
print("(Примечание: Задачи ещё не сохраняются)")

running = True
while running:
    display_menu()
    choice = input("\nВыберите опцию (1-3): ")
    
    if choice == "1":
        view_tasks()
    elif choice == "2":
        add_task()
    elif choice == "3":
        print("\nДо свидания!")
        running = False
    else:
        print("\nНеверный выбор. Пожалуйста, выберите 1-3.")

print("Спасибо за использование Менеджера списка задач!")

