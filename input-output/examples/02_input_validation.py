print()

name = input("Имя воина: ")

# Эти функции обрабатывают валидацию за нас
health = get_positive_number("Очки здоровья (1-200): ")
attack = get_positive_number("Сила атаки (1-50): ")

print()
print("=" * 40)
print(f"Воин '{name}' создан!")
print(f"Здоровье: {health}")
