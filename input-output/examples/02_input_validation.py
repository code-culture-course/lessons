"""
Пример 2: Валидация ввода

Демонстрирует:
- Обработку некорректного числового ввода
- Использование try/except для перехвата ошибок конвертации
- Запрос ввода до получения валидных данных
"""


def get_valid_number(prompt):
    """
    Продолжать запрашивать ввод, пока пользователь не предоставит валидное число.
    
    Возвращает:
        int: Проверенное число
    """
    while True:
        try:
            # Попытка конвертировать ввод в целое число
            value = int(input(prompt))
            return value
        except ValueError:
            # Это происходит, если int() не может конвертировать строку
            print("Это не валидное число. Попробуйте снова.")
            print()


def get_positive_number(prompt):
    """
    Получить положительное число от пользователя.
    
    Возвращает:
        int: Положительное число
    """
    while True:
        value = get_valid_number(prompt)
        if value > 0:
            return value
        else:
            print("Пожалуйста, введите положительное число.")
            print()


# Основная программа
print("Создайте своего воина")
