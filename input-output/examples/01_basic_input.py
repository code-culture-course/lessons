"""
Пример 1: Базовый ввод и вывод

Демонстрирует:
- Использование print() для отображения сообщений
- Использование input() для чтения пользовательского ввода
- input() всегда возвращает строку
- Конвертация строк в числа
"""

# Программа говорит
print("Добро пожаловать в Создатель персонажей!")
print()  # Пустая строка для отступа

# Программа слушает - input() всегда возвращает строку
name = input("Введите имя вашего персонажа: ")
print()

# Чтение числового ввода - конвертация из строки в int
health = int(input("Введите начальное здоровье (например, 100): "))
strength = int(input("Введите силу (например, 10): "))
print()

# Отображение результатов
print("=" * 40)
print(f"Персонаж создан!")
print(f"Имя: {name}")
print(f"Здоровье: {health}")
print(f"Сила: {strength}")
print("=" * 40)

# Вычисление чего-нибудь с числами
total_power = health + (strength * 2)
print(f"\nОбщий уровень силы: {total_power}")
