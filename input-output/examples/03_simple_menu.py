"""
Пример 3: Простое меню

Демонстрирует:
- Построение интерактивного меню
- Использование цикла while для поддержания работы меню
- Выход из цикла для завершения
"""


def display_menu():
    """Отобразить опции меню."""
    print()
    print("=" * 40)
    print("МЕНЮ ПРИКЛЮЧЕНИЙ")
    print("=" * 40)
    print("1. Исследовать лес")
    print("2. Посетить деревню")
    print("3. Проверить инвентарь")
    print("4. Выйти")
    print("=" * 40)


def explore_forest():
    """Обработка исследования леса."""
    print("\nВы отправляетесь в тёмный лес...")
    print("Вы находите 10 золотых монет!")


def visit_village():
    """Обработка посещения деревни."""
    print("\nВы входите в мирную деревню.")
    print("Жители деревни тепло приветствуют вас.")


def check_inventory():
    """Отображение инвентаря."""
    print("\nВаш инвентарь:")
    print("- Деревянный меч")
    print("- Зелье здоровья x3")
    print("- 45 золотых монет")


# Основной цикл программы
print("Добро пожаловать, смелый искатель приключений!")

running = True
while running:
    display_menu()
    
    choice = input("\nВыберите опцию (1-4): ")
    
    if choice == "1":
        explore_forest()
    elif choice == "2":
        visit_village()
    elif choice == "3":
        check_inventory()
    elif choice == "4":
        print("\nПрощайте, искатель приключений!")
        running = False  # Выход из цикла
    else:
        print("\nНеверный выбор. Пожалуйста, выберите 1-4.")

print("Спасибо за игру!")
