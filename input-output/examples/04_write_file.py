"""
Пример 4: Запись в файл

Демонстрирует:
- Открытие файла для записи
- Запись текста в файл
- Закрытие файла
- Что происходит с режимом "w" (перезапись)
"""

print("Создание файла лога игры...")

# Открыть файл для записи
# Режим "w" создаёт файл (или перезаписывает если он существует)
file = open("game_log.txt", "w")

# Записать несколько записей лога
file.write("=== Лог игровой сессии ===\n")
file.write("Игрок вошёл в мир\n")
file.write("Игрок победил гоблина\n")
file.write("Игрок нашёл сундук с сокровищами\n")
file.write("Игрок получил 50 золота\n")
file.write("Сессия завершена\n")

# Всегда закрывайте файл
file.close()

print("Лог записан в game_log.txt")
print()
print("Проверьте файл в вашей директории!")
print("Примечание: Если файл уже существовал, он был перезаписан.")
